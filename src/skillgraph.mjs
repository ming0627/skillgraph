    function wrapText(value, max) {
      const words = String(value || '').split(/\\s+/);
      const lines = [];
      let line = '';
      for (const word of words) {
        const next = line ? line + ' ' + word : word;
        if (next.length > max && line) {
          lines.push(line);
          line = word;
        } else {
          line = next;
        }
      }
      if (line) lines.push(line);
      return lines.length ? lines.slice(0, 2) : [''];
    }
    function layoutGraph() {
      const layoutNodes = nodes.filter(matchesNode);
      if (state.mode === 'spine') {
        const layout = new Map();
        const canonical = layoutNodes
          .filter(node => node.kind === 'canonical-skill')
          .sort((a, b) => a.name.localeCompare(b.name));
        const rank = new Map(canonical.map(node => [node.id, 0]));
        const spineEdges = graph.views?.workflowSpine || [];
        for (let pass = 0; pass < canonical.length; pass += 1) {
          let changed = false;
          for (const edge of spineEdges) {
            if (!rank.has(edge.source) || !rank.has(edge.target)) continue;
            const next = (rank.get(edge.source) || 0) + 1;
            if (next > (rank.get(edge.target) || 0)) {
              rank.set(edge.target, next);
              changed = true;
            }
          }
          if (!changed) break;
        }
        const columns = new Map();
        for (const node of canonical) {
          const column = rank.get(node.id) || 0;
          const list = columns.get(column) || [];
          list.push(node);
          columns.set(column, list);
        }
        const columnWidth = 280;
        const rowHeight = 106;
        const nodeWidth = 220;
        const nodeHeight = 72;
        let maxX = 0;
        let maxY = 0;
        for (const [column, list] of [...columns.entries()].sort((a, b) => a[0] - b[0])) {
          list.forEach((node, index) => {
            const x = 42 + column * columnWidth;
            const y = 72 + index * rowHeight;
            layout.set(node.id, { x, y, width: nodeWidth, height: nodeHeight });
            maxX = Math.max(maxX, x + nodeWidth);
            maxY = Math.max(maxY, y + nodeHeight);
          });
        }
        const canonicalIds = new Set(canonical.map(node => node.id));
        const attachments = layoutNodes.filter(node => !canonicalIds.has(node.id));
        for (const node of attachments) {
          const anchorEdge = edges.find(edge => (edge.source === node.id && canonicalIds.has(edge.target)) || (edge.target === node.id && canonicalIds.has(edge.source)));
          const anchorId = anchorEdge ? (canonicalIds.has(anchorEdge.source) ? anchorEdge.source : anchorEdge.target) : canonical[0]?.id;
          const anchor = layout.get(anchorId) || { x: 42, y: 72, width: nodeWidth, height: nodeHeight };
          const siblingIndex = attachments.filter(item => {
            const siblingEdge = edges.find(edge => (edge.source === item.id && canonicalIds.has(edge.target)) || (edge.target === item.id && canonicalIds.has(edge.source)));
            const siblingAnchor = siblingEdge ? (canonicalIds.has(siblingEdge.source) ? siblingEdge.source : siblingEdge.target) : canonical[0]?.id;
            return siblingAnchor === anchorId;
          }).findIndex(item => item.id === node.id);
          const x = anchor.x;
          const y = anchor.y + 118 + siblingIndex * rowHeight;
          layout.set(node.id, { x, y, width: nodeWidth, height: nodeHeight });
          maxX = Math.max(maxX, x + nodeWidth);
          maxY = Math.max(maxY, y + nodeHeight);
        }
        state.layout = layout;
        state.bounds = { x: 0, y: 0, width: maxX + 70, height: maxY + 70 };
        if (!state.viewBox) fitAll();
        return;
      }
      const layoutIds = new Set(layoutNodes.map(node => node.id));
      const rank = new Map(layoutNodes.map(node => [node.id, 0]));
      const strongEdges = edges.filter(edge => edgeAllowed(edge) && edge.type !== 'references' && layoutIds.has(edge.source) && layoutIds.has(edge.target));
      for (let pass = 0; pass < layoutNodes.length; pass += 1) {
        let changed = false;
        for (const edge of strongEdges) {
          if (!rank.has(edge.source) || !rank.has(edge.target)) continue;
          const next = Math.min(9, (rank.get(edge.source) || 0) + 1);
          if (next > (rank.get(edge.target) || 0)) {
            rank.set(edge.target, next);
            changed = true;
          }
        }
        if (!changed) break;
      }
      const connected = new Set(strongEdges.flatMap(edge => [edge.source, edge.target]));
      for (const node of layoutNodes) {
        if (!connected.has(node.id)) rank.set(node.id, Math.max(rank.get(node.id) || 0, 0));
      }
      const groups = new Map();
      for (const node of layoutNodes) {
        const key = rank.get(node.id) || 0;
        const list = groups.get(key) || [];
        list.push(node);
        groups.set(key, list);
      }
      const layout = new Map();
      const columnWidth = 270;
      const rowHeight = 100;
      const nodeWidth = 220;
      const nodeHeight = 72;
      let maxX = 0;
      let maxY = 0;
      for (const [column, list] of [...groups.entries()].sort((a, b) => a[0] - b[0])) {
        list.sort((a, b) => (a.popularityRank - b.popularityRank) || a.providerName.localeCompare(b.providerName) || a.name.localeCompare(b.name));
        list.forEach((node, index) => {
          const x = 42 + column * columnWidth;
          const y = 42 + index * rowHeight;
          layout.set(node.id, { x, y, width: nodeWidth, height: nodeHeight });
          maxX = Math.max(maxX, x + nodeWidth);
          maxY = Math.max(maxY, y + nodeHeight);
        });
      }
      state.layout = layout;
      state.bounds = { x: 0, y: 0, width: maxX + 70, height: maxY + 70 };
      if (!state.viewBox) fitAll();
    }
