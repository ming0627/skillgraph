function normalizePath(path) {
  return path.split(/[\\/]+/).join('/');
}

function slug(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'node';
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;');
}

function jsonStable(value) {
  return `${JSON.stringify(value, null, 2)}\n`;
}

function contentHash(value) {
  return createHash('sha256').update(jsonStable(value)).digest('hex').slice(0, 12);
}

function deepMerge(base, override) {
  if (!override || typeof override !== 'object') return base;
  const output = { ...base };
  for (const [key, value] of Object.entries(override)) {
    if (Array.isArray(value)) output[key] = value;
    else if (value && typeof value === 'object' && !Array.isArray(value)) {
      output[key] = deepMerge(base[key] || {}, value);
    } else output[key] = value;
  }
  return output;
}

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function parseSimpleYaml(text) {
  const metadata = {};
  let currentListKey = '';
  for (const line of text.split('\n')) {
    const listMatch = line.match(/^\s*-\s*(.+)$/);
    if (listMatch && currentListKey) {
      metadata[currentListKey].push(listMatch[1].trim().replace(/^["']|["']$/g, ''));
      continue;
    }
    const match = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!match) continue;
    currentListKey = '';
    let value = match[2].trim();
    if (!value) {
      metadata[match[1]] = [];
      currentListKey = match[1];
      continue;
    }
    if (value.startsWith('[') && value.endsWith(']')) {
      metadata[match[1]] = value
        .slice(1, -1)
        .split(',')
        .map((item) => item.trim().replace(/^["']|["']$/g, ''))
        .filter(Boolean);
      continue;
    }
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    metadata[match[1]] = value;
  }
  return metadata;
}

function parsePackageManifestText(text, filePath) {
  if (basename(filePath) === 'skillgraph.skill.json') return JSON.parse(text);
  return parseSimpleYaml(text);
}

function unique(values) {
  return [...new Set(values.filter(Boolean).map(normalizePath))];
}

function selectedProviders(config) {
  const selected = new Set(config.providers || []);
  return PROVIDER_PRESETS
    .filter((provider) => selected.has(provider.id))
    .sort((a, b) => a.popularityRank - b.popularityRank);
}

function expandProviderRoots(config) {
  return unique([
    ...selectedProviders(config).flatMap((provider) => provider.roots),
    ...(config.roots || []),
  ]);
}

function providersForRoot(rootRel, config) {
  const root = normalizePath(rootRel);
  return selectedProviders(config).filter((provider) => provider.roots.map(normalizePath).includes(root));
}

export function loadConfig(options = {}) {
  const rootDir = resolve(options.rootDir || process.cwd());
  const configPath = options.configPath
    ? resolve(rootDir, options.configPath)
    : resolve(rootDir, 'skillgraph.config.json');
  const config = existsSync(configPath)
    ? deepMerge(DEFAULT_CONFIG, readJson(configPath))
    : { ...DEFAULT_CONFIG };
  if (options.outDir) config.outDir = options.outDir;
  if (options.roots?.length) config.roots = options.roots;
  config.roots = expandProviderRoots(config);
  return {
    ...config,
    rootDir,
    outDirAbs: resolve(rootDir, config.outDir),
    configPath: existsSync(configPath) ? configPath : null,
  };
}

export function parseFrontmatter(text) {
  if (!text.startsWith('---\n')) return {};
  const end = text.indexOf('\n---', 4);
  if (end === -1) return {};
  const raw = text.slice(4, end).trim();
  const frontmatter = {};
  for (const line of raw.split('\n')) {
    const match = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!match) continue;
    let value = match[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    frontmatter[match[1]] = value;
  }
  return frontmatter;
}

function globToRegExp(pattern) {
  const input = normalizePath(pattern);
  let output = '';
  for (let index = 0; index < input.length; index += 1) {
    const char = input[index];
    const next = input[index + 1];
    if (char === '*' && next === '*') {
      output += '.*';
      index += 1;
    } else if (char === '*') {
      output += '[^/]*';
    } else if ('\\.+?^${}()|[]'.includes(char)) {
      output += `\\${char}`;
    } else {
      output += char;
    }
  }
  return new RegExp(`^${output}$`);
}

function isExcluded(repoPath, excludePatterns) {
  return excludePatterns.some((pattern) => globToRegExp(pattern).test(repoPath));
}

function isInstructionFile(filePath, config) {
  return config.instructionExtensions.includes(extname(filePath).toLowerCase());
}

function walkInstructionFiles(rootAbs, rootDir, config, currentAbs = rootAbs, result = []) {
  if (!existsSync(currentAbs)) return result;
  const stat = statSync(currentAbs);
  if (stat.isFile()) {
    result.push(currentAbs);
    return result;
  }
  for (const entry of readdirSync(currentAbs)) {
    const fullPath = join(currentAbs, entry);
    const repoPath = normalizePath(relative(rootDir, fullPath));
    if (isExcluded(repoPath, config.exclude)) continue;
    const entryStat = statSync(fullPath);
    if (entryStat.isDirectory()) {
      walkInstructionFiles(rootAbs, rootDir, config, fullPath, result);
    } else if (isInstructionFile(entry, config)) {
      result.push(fullPath);
    }
  }
  return result;
}

function inferNameFromPath(relativePath, frontmatter) {
  if (frontmatter.name) return frontmatter.name;
  const normalized = normalizePath(relativePath);
  const parts = normalized.split('/');
  if (basename(normalized) === 'SKILL.md' && parts.length >= 2) {
    return parts[parts.length - 2];
  }
  return basename(normalized, extname(normalized));
}

function classifyNode(rootRel, relativePath, frontmatter, text, config) {
  const normalizedRoot = normalizePath(rootRel);
  const normalizedPath = normalizePath(relativePath);
  if (PACKAGE_MANIFEST_NAMES.has(basename(normalizedPath))) {
    return 'skill-package';
  }
  const canonicalRoots = config.canonicalRoots.map(normalizePath);
  const wrapperRoots = config.wrapperRoots.map(normalizePath);
  if (canonicalRoots.includes(normalizedRoot) && basename(normalizedPath) === 'SKILL.md') {
    return 'canonical-skill';
  }
  const mentionsCanonicalRoot = canonicalRoots.some((root) => text.includes(`${root}/`));
  if (
    wrapperRoots.includes(normalizedRoot) &&
    (text.includes('Canonical skill instructions live at:') || mentionsCanonicalRoot)
  ) {
    return 'wrapper';
  }
  if (basename(normalizedPath) === 'SKILL.md' || frontmatter.name) {
    return 'skill';
  }
  return 'skill-doc';
}

function firstMeaningfulLine(text) {
  return text
    .split('\n')
    .map((line) => line.trim())
    .find((line) => line && !line.startsWith('---') && !line.startsWith('<!--') && !line.startsWith('#'));
}

function redact(value, terms = []) {
  let output = String(value ?? '');
  for (const term of terms.filter(Boolean)) {
    output = output.replaceAll(term, '[redacted]');
  }
  return output;
}

function redactStructured(value, terms = []) {
  if (Array.isArray(value)) return value.map((item) => redactStructured(item, terms));
  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value).map(([key, item]) => [key, redactStructured(item, terms)]),
    );
  }
  return redact(value, terms);
}

function extractDescription(text, frontmatter, config) {
  const description = frontmatter.description || firstMeaningfulLine(text) || '';
  return redact(description.slice(0, 220), config.privacy.redactTerms);
}

function collectSkillNodes(config) {
  const nodes = [];
  for (const rootRel of config.roots) {
    const rootAbs = resolve(config.rootDir, rootRel);
    const rootIsFile = existsSync(rootAbs) && statSync(rootAbs).isFile();
    for (const fullPath of walkInstructionFiles(rootAbs, config.rootDir, config)) {
      const relativeToRoot = rootIsFile
        ? basename(fullPath)
        : normalizePath(relative(rootAbs, fullPath));
      const repoPath = normalizePath(relative(config.rootDir, fullPath));
      const text = readFileSync(fullPath, 'utf8');
      const packageManifest = PACKAGE_MANIFEST_NAMES.has(basename(repoPath));
      const frontmatter = packageManifest ? parsePackageManifestText(text, repoPath) : parseFrontmatter(text);
      const name = inferNameFromPath(relativeToRoot, frontmatter);
      const kind = classifyNode(rootRel, relativeToRoot, frontmatter, text, config);
      const providers = providersForRoot(rootRel, config);
      const providerNames = providers.length ? providers.map((provider) => provider.name) : ['Custom'];
      const id = rootIsFile ? `file:${repoPath}` : `${normalizePath(rootRel)}:${relativeToRoot}`;
      nodes.push({
        id,
        name: redact(name, config.privacy.redactTerms),
        rawName: name,
        normalizedName: slug(name),
        label: redact(name, config.privacy.redactTerms),
        kind,
        providers: providers.length ? providers.map((provider) => provider.id) : ['custom'],
        provider: providers[0]?.id || 'custom',
        providerNames,
        providerName: providerNames.join(' / '),
        popularityRank: providers[0]?.popularityRank || 999,
        root: normalizePath(rootRel),
        path: repoPath,
        description: extractDescription(text, frontmatter, config),
        bytes: Buffer.byteLength(text, 'utf8'),
        tokenEstimate: Math.ceil(Buffer.byteLength(text, 'utf8') / 4),
        frontmatter: Object.fromEntries(
          Object.entries(frontmatter).map(([key, value]) => [key, redactStructured(value, config.privacy.redactTerms)]),
        ),
        text,
      });
    }
  }
  return nodes.sort((a, b) => a.path.localeCompare(b.path));
}

function addEdge(edges, source, target, type, evidence = '', config = DEFAULT_CONFIG) {
  if (!source || !target || source === target) return;
  const key = `${source}->${target}:${type}`;
  if (edges.some((edge) => edge.key === key)) return;
  edges.push({
    key,
    id: `edge-${edges.length + 1}`,
    source,
    target,
    type,
    label: type.replaceAll('-', ' '),
    evidence: config.privacy.includeEvidence
      ? redact(evidence.replace(/\s+/g, ' ').trim().slice(0, 220), config.privacy.redactTerms)
      : '',
  });
}

function evidenceNear(text, needle) {
  const index = text.toLowerCase().indexOf(String(needle).toLowerCase());
  if (index === -1) return '';
  return text.slice(Math.max(0, index - 90), Math.min(text.length, index + String(needle).length + 90));
}

function referenceTypeForEvidence(evidence) {
  if (/(before returning|route through|orchestrates|delegate|depends|invoke|run|use|calls)/i.test(evidence)) {
    return 'calls';
  }
  return 'references';
}

function createScriptNode(scriptPath, config, nodes) {
  const normalized = normalizePath(scriptPath).replace(/[.,;:]+$/g, '');
  const existing = nodes.find((node) => node.id === `script:${normalized}`);
  if (existing) return existing.id;
  const abs = resolve(config.rootDir, normalized);
  const bytes = existsSync(abs) ? Buffer.byteLength(readFileSync(abs, 'utf8'), 'utf8') : 0;
  nodes.push({
    id: `script:${normalized}`,
    name: basename(normalized),
    rawName: basename(normalized),
    normalizedName: slug(basename(normalized)),
    label: basename(normalized),
    kind: existsSync(abs) ? 'helper-script' : 'missing-helper',
    providers: ['helper'],
    provider: 'helper',
    providerNames: ['Helper Script'],
    providerName: 'Helper Script',
    popularityRank: 999,
    root: dirname(normalized),
    path: normalized,
    description: existsSync(abs) ? 'Helper script referenced by a skill.' : 'Referenced helper script was not found.',
    bytes,
    tokenEstimate: Math.ceil(bytes / 4),
    frontmatter: {},
  });
  return `script:${normalized}`;
}

function resolveHelperReference(candidate, sourceNode, config, sourceText = '') {
  const normalized = normalizePath(candidate).replace(/[.,;:]+$/g, '');
  if (!normalized.startsWith('scripts/')) return normalized;
  const matchingLines = sourceText
    .split('\n')
    .filter((line) => line.includes(candidate) || line.includes(normalized));
  for (const line of matchingLines) {
    const cdMatch = line.match(/\bcd\s+(["']?)([^"';&|`\s]+)\1/);
    if (!cdMatch) continue;
    const cwd = normalizePath(cdMatch[2]).replace(/^\.\//, '').replace(/^\.\.\//, '');
    const cwdCandidate = normalizePath(join(cwd, normalized));
    if (existsSync(resolve(config.rootDir, cwdCandidate))) return cwdCandidate;
  }
  if (existsSync(resolve(config.rootDir, normalized))) return normalized;
  const sourceRelative = normalizePath(join(dirname(sourceNode.path), normalized));
  if (existsSync(resolve(config.rootDir, sourceRelative))) return sourceRelative;
  let parent = dirname(sourceNode.path);
  while (parent && parent !== '.') {
    parent = dirname(parent);
    const parentCandidate = normalizePath(join(parent, normalized));
    if (existsSync(resolve(config.rootDir, parentCandidate))) return parentCandidate;
  }
  return normalized;
}

function helperRegexes(config) {
  const roots = [...new Set([...config.roots, ...config.canonicalRoots, ...config.wrapperRoots])].map(normalizePath);
  const rootPattern = roots.map(escapeRegExp).join('|');
  return [
    new RegExp(`((${rootPattern})/[^\\s\`'")]+/scripts/[^\\s\`'")]+)`, 'g'),
    /(?:^|[\s`'"])((?:[A-Za-z0-9_.-]+\/)+scripts\/[A-Za-z0-9_./-]+\.(?:mjs|cjs|js|sh|ts))/g,
    /(?:^|[\s`'"])((?:scripts|bin)\/[A-Za-z0-9_./-]+\.(?:mjs|cjs|js|sh|ts))/g,
  ];
}

function inferEdges(config, nodes) {
  const edges = [];
  const skillNodes = nodes.filter((node) => node.text);
  const canonicalByName = new Map();
  for (const node of skillNodes) {
    if (node.kind === 'canonical-skill' && !canonicalByName.has(node.normalizedName)) {
      canonicalByName.set(node.normalizedName, node);
    }
  }

  const canonicalRootPattern = config.canonicalRoots.map(escapeRegExp).join('|');
  const canonicalRefRegex = new RegExp(`(?:${canonicalRootPattern})/([^/\\s\`'")]+)/SKILL\\.md`, 'g');

  for (const node of skillNodes) {
    const text = node.text;
    for (const match of text.matchAll(canonicalRefRegex)) {
      const target = canonicalByName.get(slug(match[1]));
      if (target) addEdge(edges, node.id, target.id, node.kind === 'wrapper' ? 'wraps' : 'references', match[0], config);
    }

    const helperRefs = [];
    for (const pattern of helperRegexes(config)) {
      for (const match of text.matchAll(pattern)) helperRefs.push(match[1]);
    }
    for (const helperRef of [...new Set(helperRefs)]) {
      const targetId = createScriptNode(resolveHelperReference(helperRef, node, config, text), config, nodes);
      addEdge(edges, node.id, targetId, 'uses-helper', helperRef, config);
    }

    for (const target of skillNodes) {
      if (target.id === node.id) continue;
      if (node.kind === 'wrapper' && canonicalByName.get(node.normalizedName)?.id === target.id) continue;
      const isConfigNode = /\.(?:json|jsonc|ya?ml)$/i.test(target.path);
      const exactFlags = target.rawName === target.rawName.toUpperCase() ? '' : 'i';
      const exact = new RegExp(`(^|[^A-Za-z0-9_-])${escapeRegExp(target.rawName)}([^A-Za-z0-9_-]|$)`, exactFlags);
      const pathMention = new RegExp(`${escapeRegExp(target.path)}(?:\\b|$)`, 'i');
      if (isConfigNode && !pathMention.test(text)) continue;
      if (!exact.test(text) && !pathMention.test(text)) continue;
      const evidence = evidenceNear(text, target.rawName) || evidenceNear(text, target.path);
      addEdge(edges, node.id, target.id, referenceTypeForEvidence(evidence), evidence, config);
    }
  }

  return edges.sort((a, b) => `${a.source}${a.target}${a.type}`.localeCompare(`${b.source}${b.target}${b.type}`));
}

function patternFromRule(rule) {
  if (rule.regex) return new RegExp(rule.regex, rule.flags || 'i');
  if (rule.pattern) return new RegExp(escapeRegExp(rule.pattern), rule.flags || 'i');
  return null;
}

function inferIssues(config, nodes, edges) {
  const issues = [];
  const byName = new Map();
  for (const node of nodes.filter((item) => item.kind !== 'helper-script')) {
    const list = byName.get(node.normalizedName) || [];
    list.push(node);
    byName.set(node.normalizedName, list);
  }

  for (const [name, matches] of byName.entries()) {
    const canonicalCount = matches.filter((node) => node.kind === 'canonical-skill').length;
    const onlyProviderDocs = matches.every((node) => node.kind === 'skill-doc');
    if (matches.length > 1 && canonicalCount !== 1) {
      if (onlyProviderDocs) continue;
      issues.push({
        severity: canonicalCount > 1 ? 'warn' : 'info',
        type: 'duplicate-name',
        message: `Multiple skill docs share name "${name}".`,
        paths: matches.map((node) => node.path),
      });
    }
  }

  const incoming = new Map(nodes.map((node) => [node.id, 0]));
  const outgoing = new Map(nodes.map((node) => [node.id, 0]));
  for (const edge of edges) {
    incoming.set(edge.target, (incoming.get(edge.target) || 0) + 1);
    outgoing.set(edge.source, (outgoing.get(edge.source) || 0) + 1);
  }
  for (const node of nodes) {
    if (node.kind === 'canonical-skill' && !incoming.get(node.id) && !outgoing.get(node.id)) {
      issues.push({
        severity: 'info',
        type: 'orphan-skill',
        message: `${node.name} has no inferred incoming or outgoing workflow edges.`,
        paths: [node.path],
      });
    }
    if (node.kind === 'missing-helper') {
      issues.push({
        severity: 'error',
        type: 'missing-helper',
        message: `Referenced helper does not exist: ${node.path}`,
        paths: [node.path],
      });
    }
  }

  for (const rule of config.rules.stalePatterns || []) {
    const pattern = patternFromRule(rule);
    if (!pattern) continue;
    for (const node of nodes.filter((item) => item.text)) {
      if (!pattern.test(node.text)) continue;
      issues.push({
        severity: rule.severity || 'warn',
        type: rule.id || 'stale-pattern',
        message: rule.message || `Pattern matched in ${node.path}.`,
        paths: [node.path],
      });
    }
  }

  return issues.sort((a, b) => `${a.severity}${a.type}${a.message}`.localeCompare(`${b.severity}${b.type}${b.message}`));
}

function inferWorkflowSpine(nodes, edges) {
  const canonicalIds = new Set(nodes.filter((node) => node.kind === 'canonical-skill').map((node) => node.id));
  return edges
    .filter((edge) => edge.type === 'calls' && canonicalIds.has(edge.source) && canonicalIds.has(edge.target))
    .map((edge) => ({ source: edge.source, target: edge.target, type: edge.type }))
    .sort((a, b) => `${a.source}${a.target}`.localeCompare(`${b.source}${b.target}`));
}

function recommendation(type, severity, title, nodeIds, rationale) {
  return { type, severity, title, nodeIds, rationale };
}

function inferRecommendations(nodes, edges, issues) {
  const recommendations = [];
  const byId = new Map(nodes.map((node) => [node.id, node]));
  const issuesByPath = new Map();
  for (const issue of issues) {
    for (const path of issue.paths || []) {
      const list = issuesByPath.get(path) || [];
      list.push(issue);
      issuesByPath.set(path, list);
    }
    if (issue.type === 'missing-helper') {
      const node = nodes.find((item) => item.path === issue.paths?.[0]);
      if (node) {
        recommendations.push(recommendation(
          'repair-helper',
          'error',
          `Repair missing helper ${node.name}`,
          [node.id],
          'A skill references a helper script that is not present in the repository.',
        ));
      }
    }
  }

  for (const node of nodes.filter((item) => item.kind === 'canonical-skill')) {
    const incoming = edges
      .filter((edge) => edge.target === node.id && ['calls', 'references', 'wraps'].includes(edge.type))
      .map((edge) => byId.get(edge.source))
      .filter(Boolean);
    const providerNames = new Set(incoming.flatMap((source) => source.providerNames || [source.providerName]));
    if (providerNames.size >= 2) {
      recommendations.push(recommendation(
        'package-candidate',
        'info',
        `${node.name} is a reusable package candidate`,
        [node.id],
        'Multiple provider instructions or wrappers already point at this canonical skill.',
      ));
    }
  }

  for (const node of nodes.filter((item) => item.kind === 'wrapper')) {
    if (!issuesByPath.has(node.path)) continue;
    recommendations.push(recommendation(
      'wrapper-drift',
      'warn',
      `${node.name} wrapper needs drift review`,
      [node.id],
      'A wrapper has an issue; refresh it from the canonical package before reuse.',
    ));
  }

  const byName = new Map();
  for (const node of nodes.filter((item) => !['canonical-skill', 'helper-script', 'missing-helper'].includes(item.kind))) {
    const list = byName.get(node.normalizedName) || [];
    list.push(node);
    byName.set(node.normalizedName, list);
  }
  for (const [name, matches] of byName.entries()) {
    if (matches.length < 2) continue;
    recommendations.push(recommendation(
      'consolidate',
      'info',
      `Consolidate repeated ${name} instructions`,
      matches.map((node) => node.id),
      'Several non-canonical instruction files share a name and may be copies of the same workflow.',
    ));
  }

  return recommendations.sort((a, b) => `${a.severity}${a.type}${a.title}`.localeCompare(`${b.severity}${b.type}${b.title}`));
}

export function buildSkillGraph(options = {}) {
  const config = options.config?.rootDir ? options.config : loadConfig(options);
  const nodes = collectSkillNodes(config);
  const edges = inferEdges(config, nodes);
  const issues = inferIssues(config, nodes, edges);
  const publicNodes = nodes
    .map(({ text: _text, rawName: _rawName, ...node }) => node)
    .sort((a, b) => a.id.localeCompare(b.id));
  const publicEdges = edges.map(({ key: _key, ...edge }) => edge);
  const graph = {
    generatedAt: 'deterministic',
    generator: 'skillgraph',
    configPath: config.configPath ? normalizePath(relative(config.rootDir, config.configPath)) : null,
    providers: selectedProviders(config).map(({ id, name, popularityRank, popularityBasis, roots }) => ({
      id,
      name,
      popularityRank,
      popularityBasis,
      roots,
    })),
    roots: config.roots,
    summary: {
      nodeCount: publicNodes.length,
      edgeCount: edges.length,
      issueCount: issues.length,
      byKind: publicNodes.reduce((acc, node) => {
        acc[node.kind] = (acc[node.kind] || 0) + 1;
        return acc;
      }, {}),
      byProvider: publicNodes.reduce((acc, node) => {
        for (const provider of node.providerNames || [node.providerName]) {
          acc[provider] = (acc[provider] || 0) + 1;
        }
        return acc;
      }, {}),
    },
    nodes: publicNodes,
    edges: publicEdges,
    issues,
    views: {
      workflowSpine: inferWorkflowSpine(publicNodes, publicEdges),
    },
    recommendations: inferRecommendations(publicNodes, publicEdges, issues),
  };
  return {
    ...graph,
    contentHash: contentHash({
      generator: graph.generator,
      configPath: graph.configPath,
      providers: graph.providers,
      roots: graph.roots,
      summary: graph.summary,
      nodes: graph.nodes,
      edges: graph.edges,
      issues: graph.issues,
      views: graph.views,
      recommendations: graph.recommendations,
    }),
  };
}

function mermaidId(index) {
  return `N${index}`;
}

function mermaidLabel(node) {
  return `${node.name}<br/><small>${node.kind}</small>`;
}

export function generateMermaid(graph) {
  const selectedEdges = graph.edges.filter((edge) => edge.type !== 'references');
  const connected = new Set(selectedEdges.flatMap((edge) => [edge.source, edge.target]));
  const nodes = graph.nodes.filter((node) => connected.has(node.id));
  const idMap = new Map(nodes.map((node, index) => [node.id, mermaidId(index)]));
  const lines = [
    'flowchart LR',
    '  classDef canonical fill:#dbeafe,stroke:#2563eb,color:#172554',
    '  classDef wrapper fill:#fef3c7,stroke:#d97706,color:#451a03',
    '  classDef helper fill:#dcfce7,stroke:#16a34a,color:#052e16',
    '  classDef doc fill:#f3f4f6,stroke:#6b7280,color:#111827',
  ];
  for (const node of nodes) lines.push(`  ${idMap.get(node.id)}["${mermaidLabel(node)}"]`);
  for (const edge of selectedEdges) {
    if (!idMap.has(edge.source) || !idMap.has(edge.target)) continue;
    lines.push(`  ${idMap.get(edge.source)} -- "${edge.label}" --> ${idMap.get(edge.target)}`);
  }
  for (const node of nodes) {
    const cls = node.kind === 'canonical-skill'
      ? 'canonical'
      : node.kind === 'wrapper'
        ? 'wrapper'
        : node.kind.includes('helper')
          ? 'helper'
          : 'doc';
    lines.push(`  class ${idMap.get(node.id)} ${cls}`);
  }
  return lines.join('\n');
}

export function generateMarkdown(graph) {
  const issueLines = graph.issues.length
    ? graph.issues.map((issue) => `- **${issue.severity.toUpperCase()} ${issue.type}:** ${issue.message}`).join('\n')
    : '- No issues detected.';
  const providerRows = graph.providers.length
    ? graph.providers
      .map((provider) => `| ${provider.popularityRank} | ${provider.name} | \`${provider.roots.join('`, `')}\` | ${provider.popularityBasis} |`)
      .join('\n')
    : '| - | Custom | - | No provider presets selected. |';
  const nodeRows = graph.nodes
    .map((node) => `| \`${node.name}\` | ${node.providerName} | ${node.kind} | \`${node.path}\` | ${node.tokenEstimate} |`)
    .join('\n');
  const edgeRows = graph.edges
    .map((edge) => {
      const source = graph.nodes.find((node) => node.id === edge.source)?.name || edge.source;
      const target = graph.nodes.find((node) => node.id === edge.target)?.name || edge.target;
      return `| \`${source}\` | ${edge.type} | \`${target}\` | ${edge.evidence || ''} |`;
    })
    .join('\n');
  return `${AUTO_MARKER}
# Skillgraph

Generated: ${graph.generatedAt}
Content hash: \`${graph.contentHash}\`

## Summary

- Nodes: ${graph.summary.nodeCount}
- Edges: ${graph.summary.edgeCount}
- Issues: ${graph.summary.issueCount}
- Kinds: ${Object.entries(graph.summary.byKind).map(([kind, count]) => `${kind}=${count}`).join(', ')}
- Providers: ${Object.entries(graph.summary.byProvider).map(([provider, count]) => `${provider}=${count}`).join(', ')}

## Provider Coverage

| Rank | Provider | Default roots | Basis |
| ---: | --- | --- | --- |
${providerRows}

## Workflow Map

\`\`\`mermaid
${generateMermaid(graph)}
\`\`\`

## Issues

${issueLines}

## Nodes

| Name | Provider | Kind | Path | Est. Tokens |
| --- | --- | --- | --- | ---: |
${nodeRows}

## Edges

| Source | Type | Target | Evidence |
| --- | --- | --- | --- |
${edgeRows}
`;
}

